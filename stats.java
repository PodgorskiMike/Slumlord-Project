package Slumlord;

public class stats {
	
	protected String name;
	protected int totG;
	protected int wins;
	protected int Hscore;
	protected int Tscore;
	
	public stats(String name, int totG, int wins, int Hscore, int Tscore )
	{
		this.name = name;
		this.totG = totG;
		this.wins = wins;
		this.Hscore = Hscore;
		this.Tscore = Tscore;
		
	}

}
